package com.adaivskenan.test.cp07_nullsafety;

/*
 * @Nullable: Annotation to indicate that a specific parameter, return value, or
 * field can be null.
 * 
 * @NonNull: Annotation to indicate that a specific parameter, return value, or
 * field cannot be null (not needed on parameters / return values and fields
 * where @NonNullApi and @NonNullFields apply, respectively).
 * 
 * @NonNullApi: Annotation at the package level that declares non-null as the
 * default semantics for parameters and return values.
 * 
 * @NonNullFields: Annotation at the package level that declares non-null as the
 * default semantics for fields.
 */